// Regenerates the three build-time sync inputs from the freshly-built dist/.
// Wired into cfg.buildCmd, so every sync (and every driver run) re-derives them.
// All three are GITIGNORED:
//   - index.d.ts would otherwise enter Paris's broad tsconfig `include` (**/*.d.ts)
//     and break `tsc --noEmit` whenever dist/ isn't built first.
//   - the barrels & preview-base.css are deterministic from dist/ + themes.ts.
// Run via bun (transpiles the .ts theme import).
import { generateCSS } from 'pte';
import { LightTheme } from '../src/stories/theme/themes.ts';
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url)); // .design-sync
const root = dirname(here);                            // repo root
const storiesDir = join(root, 'dist', 'stories');
const has = (d, f) => existsSync(join(storiesDir, d, f));
const jsDirs = readdirSync(storiesDir).filter((d) => has(d, 'index.js')).sort();
const dtDirs = readdirSync(storiesDir).filter((d) => has(d, 'index.d.ts')).sort();

// 1. JS barrel → window.Paris bundle entry (cfg.entry)
writeFileSync(join(here, 'paris-entry.mjs'),
  '// AUTO-GENERATED by .design-sync/gen-sync-inputs.mjs (cfg.buildCmd). Gitignored; do not edit.\n' +
  jsDirs.map((d) => `export * from '../dist/stories/${d}/index.js';`).join('\n') + '\n');

// 2. Types barrel at repo root → projectFor's default <pkgDir>/index.d.ts entry
writeFileSync(join(root, 'index.d.ts'),
  '// AUTO-GENERATED by .design-sync/gen-sync-inputs.mjs (cfg.buildCmd). Gitignored; do not edit/commit.\n' +
  dtDirs.map((d) => `export * from './dist/stories/${d}/index';`).join('\n') + '\n');

// 3. Preview base CSS = current LightTheme vars + the global preflight (cfg.cssEntry)
const vars = generateCSS(LightTheme, ':root');
let preflight = '';
try { preflight = readFileSync(join(storiesDir, 'theme', 'global.css'), 'utf8'); }
catch { console.warn('[gen-sync-inputs] dist/stories/theme/global.css missing — run the DS build first'); }
writeFileSync(join(here, 'preview-base.css'),
  `${vars}\n/* ---- Paris global preflight (paris/theme/global.css) ---- */\n${preflight}`);

console.log(`[gen-sync-inputs] paris-entry.mjs (${jsDirs.length}) + index.d.ts (${dtDirs.length}) + preview-base.css (${vars.length + preflight.length}B)`);
